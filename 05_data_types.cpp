#include <iostream>
#include <string>
using namespace std;

int main()
{
  // Numeric data types
  int a = 5;
  cout << a << "\n";

  float b = 55.5;
  cout << b << "\n";

  float c = 2e5;
  cout << c << "\n";

  double d = 3.141562859;
  cout << d << "\n";

  // Boolean data type
  bool conditionA = true;
  cout << conditionA << " ";

  bool conditionB = false;
  cout << conditionB << "\n";

  // Character data type
  char charA = 'A';
  cout << charA << " ";

  char charB = 'B';
  cout << charB << "\n";

  // String data type
  string name = "IndieDev";
  cout << name << "\n";

  // auto keyword
  auto n = 5;
  cout << n << "\n";

  return 0;
}